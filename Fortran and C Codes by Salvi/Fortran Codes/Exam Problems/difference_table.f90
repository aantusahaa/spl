INTEGER A,D1A,D2A,D3A
DIMENSION A(10),D1A(8),D2A(8),D3A(7)
DATA A /2,8,-3,5,9,-4,8,0,-8,16/

!FIRST DIFFERENCE
N1=9
DO I=1,N1
    D1A(I)=A(I+1)-A(I)
END DO
!SECOND DIFFERENCE
N2=8
DO I=1,N2
    D2A(I)=D1A(I+1)-D1A(I)
END DO
!THIRD DIFFERENCE
N3=7
DO I=1,N3
    D3A(I)=D2A(I+1)-D2A(I)
END DO

PRINT*,"THE DIFFERENCE TABLE UP TO 3RD DIFFERENCE IS:"
WRITE(*,10)A
WRITE(*,20)D1A
WRITE(*,30)D2A
WRITE(*,40)D3A
10 FORMAT(10(1X,I8))
20 FORMAT(5X,9(1X,I8))
30 FORMAT(10X,8(1X,I8))
40 FORMAT(15X,7(1X,I8))

STOP
END